package com.example.hiep.bds.model;

public class Dis {
    Integer id;
    Integer met;

    public Dis(Integer id, Integer met) {
        this.id = id;
        this.met = met;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getMet() {
        return met;
    }

    public void setMet(Integer met) {
        this.met = met;
    }
}
